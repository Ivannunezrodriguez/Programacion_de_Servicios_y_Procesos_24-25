public class Triangulo
{
    public static void main(String[] args) {
        if (args.length==0){
            System.out.println("se requiere un argumento");
            return;
        }
        int filas=Integer.parseInt(args[0]);
        for (int i = filas;i>1;i--){
            for (int n =1; n <=1;n++){
                System.out.println(n);
            }

            System.out.println();
        }
    }
}
